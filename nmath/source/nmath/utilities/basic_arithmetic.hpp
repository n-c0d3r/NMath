	inline f32 round_to_nearest(f32 x) noexcept {

        float i = floorf(x);
        x -= i;
        if (x < 0.5f)
            return i;
        if (x > 0.5f)
            return i + 1.f;

        float int_part;
        (void)modff(i / 2.f, &int_part);
        if ((2.f * int_part) == i)
        {
            return i;
        }

        return i + 1.f;
	}

    inline f32 reinterpret_cast_i32_to_f32(i32 a) noexcept {

        union {
            f32 f;
            i32 i;
        } temp;

        temp.i = a;

        return temp.f;
    }
    inline f32 reinterpret_cast_u32_to_f32(u32 a) noexcept {

        union {
            f32 f;
            u32 u;
        } temp;

        temp.u = a;

        return temp.f;
    }

